    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed

        int cedula = Integer.parseInt(JOptionPane.showInputDialog("Cedula del estudiante: "));
        boolean serepite = false;
        for (int i = 0; i < table.getRowCount(); i++) {
            if (cedula == Integer.parseInt((String) table.getValueAt(i, 0))) {
                serepite = true;
            }
        }

        if (serepite) {
            JOptionPane.showMessageDialog(null, "ALERTA!!!"
                    + "\n\nPaciente ya vacunado, NO PUEDE SER VACUNADO NUEVAMENTE!!!");
        } else {
            String gener = genero.getSelectedItem().toString();

            //JOptionPane.showMessageDialog(null, "Fecha actual: " + fechaactual3.getSelectedItem());
            int añoactual = fechaactual3.getCalendar().getWeekYear();
            int añonaci = fechadenacimiento3.getCalendar().getWeekYear();

            /*DateTimeFormatter date = DateTimeFormatter.ofPattern("dd/dd/mm/yyyy");
        LocalDate fechanacimiento = LocalDate.parse((CharSequence) fechadenacimiento3.getSelectedItem(), date);
        LocalDate fechactual = LocalDate.parse((CharSequence) fechaactual3.getSelectedItem(), date);*/
            int edad = N_Programa_Vac.calculoedad(añonaci, añoactual);

            //JOptionPane.showMessageDialog(null, "Edad: " + edad + " años.");
            int codigo = N_Programa_Vac.vacunar(edad, genero.getSelectedIndex());
            String vacuna = "";

            if (codigo == 01) {
                vacuna = "Paperas";
            } else if (codigo == 02) {
                vacuna = "Sarampión";
            }
            if (codigo == 03) {
                vacuna = "Influenza";
            }
            if (codigo == 04) {
                vacuna = "Hepatitis";
            } else if (codigo == 05) {
                vacuna = "Fiebre Amarilla";
            }

            String Datos[] = {String.valueOf(cedula), gener, String.valueOf(edad), String.valueOf(codigo), vacuna};
            modelo.addRow(Datos);
            calculate.setEnabled(false);
        }
        calculate.setEnabled(false);
        //calculate.setOpaque(true);
    }//GEN-LAST:event_calculateActionPerformed

