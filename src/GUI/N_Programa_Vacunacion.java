    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed

        int selectedfiles = table.getSelectedRowCount();
        if (selectedfiles > 0) {
            String botones[] = {"  Sí   ", "   No   "};
            int eleccion = JOptionPane.showOptionDialog(this, "¿Desea eliminar la/s fila/s seleccionada/s?", "Título", 0, 0, null, botones, this);

            if (eleccion == JOptionPane.YES_OPTION) {
                //JOptionPane.showMessageDialog(null, "Las filas que se eliminarán son la fila: " + Arrays.toString(table.getSelectedRows()));
                //ArrayList<Integer> filasseleccionadas = new ArrayList<Integer>();
                int filasseleccionadas[] = new int[table.getSelectedRows().length];
                filasseleccionadas = table.getSelectedRows();

                //JOptionPane.showMessageDialog(null, "Cantidad de filas a eliminar: " + filasseleccionadas.length);
                for (int i = filasseleccionadas.length - 1; i >= 0; i--) {
                    //JOptionPane.showMessageDialog(null, "Fila " + filasseleccionadas[i]);
                    //JOptionPane.showMessageDialog(null, "fila a eliminar AHORA MISMO: " + filasseleccionadas[i]);
                    modelo.removeRow(filasseleccionadas[i]);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay fila/s seleccionada/s para eliminar"
                    + "\nPor favor seleccione!");
        }
    }//GEN-LAST:event_EliminarActionPerformed

